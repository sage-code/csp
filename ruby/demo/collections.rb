#array of integers
v = [1,2,3] 
print "v=",v, "\n"
print "v[0]=", v[0], "\n" #first element
print "v[-1]=",v[-1], "\n"#last element

#array of strings
z = ['a','b','x'] 
print "z=",z, "\n"

#array of symbols
u = [:a,:b,:c] 
print "u=", u, "\n"

#hash of strings
o = {'a' => 1, 'b' => 2, 'c' => 3}
print "o=", o, "\n"

#hash of symbols
q = {:a => 1, :b => 2, :c => 3}
print "q=", q, "\n"
